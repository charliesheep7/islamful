export type ArticleType = 'is-x-haram' | 'dua' | 'how-to' | 'explainer'

export const PROMPTS: Record<ArticleType, string> = {
  'is-x-haram': IS_X_HARAM_PROMPT,
  dua: DUA_PROMPT,
  'how-to': HOW_TO_GUIDE_PROMPT,
  explainer: EXPLAINER_PROMPT,
}

/**
 * Get the appropriate prompt for an article type.
 *
 * @example
 * const prompt = getPrompt('is-x-haram')
 * // Pass to your LLM: `${prompt}\n\nTopic: Is vaping haram in Islam?`
 */
export function getPrompt(type: ArticleType): string {
  return PROMPTS[type]
}

/**
 * Auto-detect article type from a topic string.
 *
 * @example
 * detectArticleType("Is tattoo haram?")       // 'is-x-haram'
 * detectArticleType("Dua before sleeping")     // 'dua'
 * detectArticleType("How to pray Isha")        // 'how-to'
 * detectArticleType("Ramadan in Islam")        // 'explainer'
 */
export function detectArticleType(topic: string): ArticleType {
  const lower = topic.toLowerCase()
  if (/\b(haram|halal|permissible|forbidden|allowed|sinful)\b/.test(lower)) return 'is-x-haram'
  if (/\b(dua|supplication|prayer for|dhikr)\b/.test(lower)) return 'dua'
  if (/\b(how to|guide|steps?|tutorial)\b/.test(lower)) return 'how-to'
  return 'explainer'
}
